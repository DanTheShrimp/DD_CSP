#DD Period 7 My Family Loop

import time
family = ["Lizzie", "Sarah", "Dave"]
slugs = ["Doomslug", "Chubs", "Fine", "Boomslug", "Bubbles", "Rose", "Boomerslug"]

for member in family:
    print(f"Hi, {member}! I am questing for a snack!")
    time.sleep(1)

for slug in slugs:
    print(f"Awwwww, you're so cute, {slug}! Here, have a mushroom.")
    time.sleep(1)