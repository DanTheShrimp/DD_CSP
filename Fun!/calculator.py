first_number = int(input("What is the first number?"))
middle_thing = input("What is the operation? (+,-,/, or *)")
last_number = int(input("What is the last number?"))

if middle_thing == "+":
    print(first_number+last_number)

elif middle_thing == "-":
    print(first_number-last_number)

elif middle_thing == "/":
    print(first_number/last_number)

elif middle_thing == "*":
    print(first_number*last_number)
