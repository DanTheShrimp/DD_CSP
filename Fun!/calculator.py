first_number = input("What is the first number?")
middle_thing = input("What is the middle thing? (+,-,/, or *)")
last_number = input("What is the last number?")

if middle_thing == "+":
    print(first_number + last_number)

elif middle_thing == "-":
    print(first_number - last_number)

elif middle_thing == "/":
    print(first_number / last_number)

elif middle_thing == "*":
    print(first_number * last_number)
